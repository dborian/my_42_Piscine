#include "../includes/bsq.h"

void	ft_free(char **strs, int *tab)
{
	int	i;
	int	len;

	i = 0;
	len = ft_atoi(strs[0], '\0');
	free(tab);
	while (i <= len)
		free(strs[i++]);
	free(strs);
}
